/** Глобальная конфигурация симуляции и визуализации Barnes–Hut. */
object Config {
    // Окно
    /** Ширина окна визуализации в пикселях. */
    var WIDTH_PX = 2400

    /** Высота окна визуализации в пикселях. */
    var HEIGHT_PX = 800

    /** Гравитационная постоянная в условных единицах. */
    var G = 80.0

    /** Шаг интегрирования по времени. */
    var DT = 0.015

    /** Значение сглаживания силы на малых расстояниях. */
    var SOFTENING = 1.0

    /** Квадрат сглаживания, используемый в расчётах. */
    val SOFT2 = SOFTENING * SOFTENING

    /** Параметр тэта для критерия Барнса–Хатта. */
    var theta: Double = 0.40  // Barnes–Hut θ

    /** Радиус создаваемого диска при клике. */
    var r = 100.0

    /** Количество тел, добавляемых при создании нового диска. */
    var n = 2000

    // Начальные количества/массы
    /** Количество тел в стартовой сцене. */
    var BODIES_COUNT = 3000

    /** Масса центрального тела кеплеровского диска. */
    const val CENTRAL_MASS = 50_000.0

    /** Минимально допустимый радиус спутника от центра. */
    const val MIN_R = 24.0

    // Для «кеплера» — фиксируем общую массу диска, чтобы поведение не зависело от N
    /** Суммарная масса спутников кеплеровского диска. */
    const val TOTAL_SATELLITE_MASS = 1000.0

    /** Список предопределённых сцен симуляции. */
    enum class Scene { KEPLER_DISK, KEPLER_DISK_COLLIDER }

    /** Текущая выбранная сцена. */
    var scene: Scene = Scene.KEPLER_DISK
}